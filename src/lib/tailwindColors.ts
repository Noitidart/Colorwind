// Keep the old name as an alias for backward compatibility.
export const TAILWIND_COLORS: readonly TailwindColor[] = TAILWIND_COLORS_V4;

// Pre-built immutable bundle: all derived data for a palette computed once.
export type PaletteBundle = {
  version: Version;
  colors: readonly TailwindColor[];
  byName: ReadonlyMap<string, string>;
  referenceColors: readonly ReferenceColor[];
};

function buildBundle(colors: readonly TailwindColor[], version: Version): PaletteBundle {
  const byName = new Map(colors.map((c) => [c.name, c.value]));
  const referenceColors: ReferenceColor[] = [];
  for (const entry of colors) {
    const color = parse(entry.value);
    if (color) {
      referenceColors.push({ name: entry.name, value: entry.value, color });
    }
  }
  return { version, colors, byName, referenceColors };
}

export const V3_BUNDLE = buildBundle(TAILWIND_COLORS_V3, "v3");
export const V4_BUNDLE = buildBundle(TAILWIND_COLORS_V4, "v4");

export function getPaletteForVersion(version: Version): PaletteBundle {
  return version === "v3" ? V3_BUNDLE : V4_BUNDLE;
}
